public class AngleInfo {
    private double startAngle;
    private double angle;

    public AngleInfo(double startAngle, double angle) {
        this.startAngle = startAngle;
        this.angle = angle;
    }

    public double getStartAngle() {
        return startAngle;
    }

    public double getAngle() {
        return angle;
    }
}
