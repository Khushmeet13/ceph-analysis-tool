import javafx.scene.shape.Line;

public class Triangle {
    private Line line1;
    private Line line2;
   

    public Triangle() {
        this.line1 = new Line();
        this.line2 = new Line();
        
    }

    public Line getLine1() {
        return line1;
    }

    public Line getLine2() {
        return line2;
    }

   

    
}
